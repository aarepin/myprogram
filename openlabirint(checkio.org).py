def checkio(mm):
    ############### 
    ## ping exit ##
    ###############
    curr=[[1,1]]
    d=0
    while curr:
        c1=[]
        for i in curr:
            if mm[i[0]+1][i[1]]==0:
                mm[i[0]+1][i[1]]=mm[i[0]][i[1]]-1
                c1=c1+[[i[0]+1,i[1]]]
                
            if mm[i[0]][i[1]+1]==0:
                mm[i[0]][i[1]+1]=mm[i[0]][i[1]]-1
                c1=c1+[[i[0],i[1]+1]]
                
            if mm[i[0]-1][i[1]]==0:
                mm[i[0]-1][i[1]]=mm[i[0]][i[1]]-1
                c1=c1+[[i[0]-1,i[1]]]
            
            if mm[i[0]][i[1]-1]==0:
                mm[i[0]][i[1]-1]=mm[i[0]][i[1]]-1
                c1=c1+[[i[0],i[1]-1]]
        curr=c1
    #############

    #############################
    ## go home by shortest way ##
    #############################
    path=''
    mm[1][1]=0
    i=[10,10]
    while i!=[1,1]:
        if 1 > mm[i[0]-1][i[1]] > mm[i[0]][i[1]]:
            i[0]=i[0]-1
            path='S'+path
        if 1 > mm[i[0]][i[1]-1] > mm[i[0]][i[1]]:
            i[1]=i[1]-1
            path='E'+path
        if 1 > mm[i[0]+1][i[1]] > mm[i[0]][i[1]]:
            i[0]=i[0]+1
            path='N'+path
        if 1 > mm[i[0]][i[1]+1] > mm[i[0]][i[1]]:
            i[1]=i[1]+1
            path='W'+path
    return path


